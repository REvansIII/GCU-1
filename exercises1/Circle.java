public class Circle {

	public static void main(String[] args) {
	double radius = 5.5; // assign value for radius
	final double PI = 3.14159; // declare constant 
	
	System.out.println("The perimeter of a circle with a radius of 5.5 is");
	System.out.println(2*radius*PI);
	System.out.println("The area of a circle with a radius of 5.5 is");
	System.out.println(radius*radius*PI);


	}

}
